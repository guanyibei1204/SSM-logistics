package com.yong.Pojo;

public class Province {
    private Integer id;
    private String name;
    private String captial;
    private double captial_lo;
    private double captial_la;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCaptial() {
        return captial;
    }

    public void setCaptial(String captial) {
        this.captial = captial;
    }

    public double getCaptial_lo() {
        return captial_lo;
    }

    public void setCaptial_lo(double captial_lo) {
        this.captial_lo = captial_lo;
    }

    public double getCaptial_la() {
        return captial_la;
    }

    public void setCaptial_la(double captial_la) {
        this.captial_la = captial_la;
    }
}
